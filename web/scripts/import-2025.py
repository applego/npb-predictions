VALID_PACIFIC = {"ソフトバンク", "オリックス", "ロッテ", "楽天", "日本ハム", "西武"}


def normalize_source(raw_source):
    """Normalize source to standard categories."""
    if raw_source is None:
        return None
    return SOURCE_MAP.get(raw_source, raw_source)


def validate_teams(teams, valid_set, label):
    """Validate that all teams are in the valid set."""
    if teams is None:
        return True
    for t in teams:
        if t not in valid_set:
            print(f"  WARNING: Invalid {label} team '{t}'")
            return False
    if len(teams) != 6:
        print(f"  WARNING: {label} has {len(teams)} teams, expected 6")
        return False
    return True


def main():
    conn = sqlite3.connect(DB_PATH)
    cur = conn.cursor()

    # Get 2025 season id
    cur.execute("SELECT id FROM seasons WHERE year = 2025")
    row = cur.fetchone()
    if not row:
        print("ERROR: Season 2025 not found in database")
        return
    season_id = row[0]
    print(f"Season 2025 id: {season_id}")

    # Check if any 2025 predictions already exist
    cur.execute(
        "SELECT COUNT(*) FROM predictions WHERE season_id = ?", (season_id,)
    )
    existing = cur.fetchone()[0]
    if existing > 0:
        print(f"WARNING: {existing} predictions already exist for 2025. Skipping import.")
        print("  To reimport, delete existing 2025 predictions first.")
        conn.close()
        return

    total_users = 0
    total_preds = 0
    total_picks = 0
    skipped = 0

    for name, variant, raw_source, central, pacific in RAW_PREDICTIONS:
        source = normalize_source(raw_source)

        # Validate teams
        if central is not None and not validate_teams(central, VALID_CENTRAL, f"{name} central"):
            skipped += 1
            continue
        if pacific is not None and not validate_teams(pacific, VALID_PACIFIC, f"{name} pacific"):
            skipped += 1
            continue

        # Skip entries with neither league
        if central is None and pacific is None:
            skipped += 1
            continue

        # Build slug
        slug = name
        if variant:
            slug = f"{name}-{variant}"

        # Find or create user
        if variant:
            cur.execute(
                "SELECT id FROM users WHERE name = ? AND variant = ?",
                (name, variant)
            )
        else:
            cur.execute(
                "SELECT id FROM users WHERE name = ? AND (variant IS NULL OR variant = '')",
                (name,)
            )
        user_row = cur.fetchone()

        if not user_row:
            # Ensure slug uniqueness
            final_slug = slug
            counter = 1
            while True:
                cur.execute("SELECT id FROM users WHERE slug = ?", (final_slug,))
                if not cur.fetchone():
                    break
                final_slug = f"{slug}-{counter}"
                counter += 1

            cur.execute(
                "INSERT INTO users (name, slug, role, source, variant, created_at) VALUES (?, ?, 'commentator', ?, ?, unixepoch())",
                (name, final_slug, source, variant)
            )
            user_id = cur.lastrowid
            total_users += 1
        else:
            user_id = user_row[0]
            # Update source if we have new info
            if source:
                cur.execute(
                    "UPDATE users SET source = ? WHERE id = ? AND (source IS NULL OR source = '')",
                    (source, user_id)
                )

        # Create prediction
        cur.execute(
            "INSERT INTO predictions (user_id, season_id, is_locked, created_at, updated_at) VALUES (?, ?, 1, unixepoch(), unixepoch())",
            (user_id, season_id)
        )
        prediction_id = cur.lastrowid
        total_preds += 1

        # Insert ranking picks
        if central:
            for rank, team_name in enumerate(central, 1):
                cur.execute(
                    "INSERT INTO ranking_picks (prediction_id, league, rank, team_name) VALUES (?, 'central', ?, ?)",
                    (prediction_id, rank, team_name)
                )
                total_picks += 1

        if pacific:
            for rank, team_name in enumerate(pacific, 1):
                cur.execute(
                    "INSERT INTO ranking_picks (prediction_id, league, rank, team_name) VALUES (?, 'pacific', ?, ?)",
                    (prediction_id, rank, team_name)
                )
                total_picks += 1

    conn.commit()
    conn.close()

    print(f"\nImport complete:")
    print(f"  New users: {total_users}")
    print(f"  Predictions: {total_preds}")
    print(f"  Ranking picks: {total_picks}")
    print(f"  Skipped: {skipped}")


if __name__ == "__main__":
    main()
